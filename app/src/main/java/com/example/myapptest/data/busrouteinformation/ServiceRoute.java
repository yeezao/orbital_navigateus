package com.example.myapptest.data.busrouteinformation;

public class ServiceRoute {

    private String stopCaption;
    private String stopId;
    private Double stopLat;
    private Double stopLong;

    public String getStopCaption() {
        return stopCaption;
    }

    public void setStopCaption(String stopCaption) {
        this.stopCaption = stopCaption;
    }

    public String getStopId() {
        return stopId;
    }

    public void setStopId(String stopId) {
        this.stopId = stopId;
    }

    public Double getStopLat() {
        return stopLat;
    }

    public void setStopLat(Double stopLat) {
        this.stopLat = stopLat;
    }

    public Double getStopLong() {
        return stopLong;
    }

    public void setStopLong(Double stopLong) {
        this.stopLong = stopLong;
    }


}
